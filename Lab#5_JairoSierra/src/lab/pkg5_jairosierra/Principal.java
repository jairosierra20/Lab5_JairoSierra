    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
       int w = cb_usuarios5.getSelectedIndex();
       ArrayList<Mazos> mazo = lista.get(w).getMazo();
       DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_mazos.getModel();
        modelo.addElement(mazo);
        cb_mazos.setModel(modelo);
       if(cb_mazos.getSelectedIndex()>=0){
           DefaultTreeModel modeloArbol = (DefaultTreeModel) jt_cartas.getModel();
           DefaultMutableTreeNode raiz = (DefaultMutableTreeNode)modeloArbol.getRoot();
           String tipo="",nombre="";
           String dano,vida;
           dano=((Carta)lista2.get(cb_mazos.getSelectedIndex())).getDano();
           vida = ((Carta)lista2.get(cb_mazos.getSelectedIndex())).getVida();
           nombre = ((Carta)lista2.get(cb_mazos.getSelectedIndex())).getNombre();
           if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Minipekka")){
               tipo="Especial";
           }
           if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Montapuerco")){
               tipo="Especial";
           }
           if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Golem")){
           tipo="Epica";
       }
          if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Lenador")){
              tipo="Legandaria";
          } 
          if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Dragon_Infernal")){
              tipo="Legendaria";
          }
          if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Dragon")){
              tipo="Epica";
          }
          if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Gigante_Noble")){
              tipo="Comun";
          }
          if(cb_mazos.getSelectedItem().getClass().getSimpleName().equals("Pandilla_Duendes")){
              tipo="Comun";
          }
          int centinela=-1;
           for (int i = 0; i < raiz.getChildCount(); i++) {
               if(raiz.getChildAt(i).toString().equals(tipo)){
                   
                   for (int j = 0; j < raiz.getChildAt(i).getChildCount(); j++) {
                       if(raiz.getChildAt(i).getChildAt(j).toString().equals(nombre) && raiz.getChildAt(i).equals(tipo)){
                       i=raiz.getChildCount();
                       j=raiz.getChildAt(i).getChildCount();
                       }
                   }
                   DefaultMutableTreeNode q = new DefaultMutableTreeNode(new Carta(nombre,dano,vida));
                   ((DefaultMutableTreeNode) raiz.getChildAt(i)).add(q); 
                   centinela=1;
               }
           }
           if(centinela == -1){
               DefaultMutableTreeNode n = new DefaultMutableTreeNode(tipo);
               DefaultMutableTreeNode e = new DefaultMutableTreeNode(new Carta(nombre,dano,vida));
               n.add(e);
               raiz.add(n);
           }
       }
    }//GEN-LAST:event_jButton5ActionPerformed

